local function lookup(key)
    local lang = current_lang
    if t[lang] and t[lang][key] then return t[lang][key] end
    lang = lang2
    if t[lang] and t[lang][key] then return t[lang][key] end
    return nil
end

local function tr(text)
    local r = lookup(text)
    if r then return r end
    return gettext(text)
end

local M = {}
setmetatable(M, { __call = function(_, ...) return tr(...) end })

return M
